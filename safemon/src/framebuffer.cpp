Framebuffer::Framebuffer(uint32_t* map, uint32_t width,
                         uint32_t height, uint32_t pitch)
    : map_(map), width_(width), height_(height), pitch_(pitch) {}

void Framebuffer::draw_pixel(int x, int y, uint32_t color) {
    if (x < 0 || y < 0 || (uint32_t)x >= width_ || (uint32_t)y >= height_)
        return;
    // pitch is in bytes, map_ is uint32_t* so divide by 4
    map_[(y * pitch_ / 4) + x] = color;
}

void Framebuffer::fill(uint32_t color) {
    for (uint32_t y = 0; y < height_; y++)
        for (uint32_t x = 0; x < width_; x++)
            draw_pixel(x, y, color);
}

void Framebuffer::draw_rect(int x, int y, int w, int h, uint32_t color) {
    for (int row = y; row < y + h; row++)
        for (int col = x; col < x + w; col++)
            draw_pixel(col, row, color);
}

void Framebuffer::draw_text(int x, int y, const std::string& text,
                             uint32_t color, int scale) {
    int cx = x;
    for (char c : text) {
        if ((uint8_t)c >= 128) continue;
        const uint8_t* glyph = FONT[(uint8_t)c];
        for (int row = 0; row < 7; row++) {
            for (int col = 0; col < 5; col++) {
                if (glyph[row] & (0x10 >> col)) {
                    for (int sy = 0; sy < scale; sy++)
                        for (int sx = 0; sx < scale; sx++)
                            draw_pixel(cx + col*scale + sx,
                                       y + row*scale + sy, color);
                }
            }
        }
        cx += 6 * scale;
    }
}